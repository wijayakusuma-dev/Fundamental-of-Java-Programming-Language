public class perulanganfor {
	public static void main (String[]args){
		for (int i=1; i<=3; i++) {
				for (int j=1; j<=i ; j++){
					System.out.print("#");
				}
					System.out.println(" ");
			}
			System.out.println("");
			for (int x=3; x>=1; x--){
				for (int y=2; y>=x; y--){
					System.out.print(" ");
				}
				for (int z=1; z<=x; z++){
					System.out.print("#");
				}
				System.out.println("");
				}
			}
}







